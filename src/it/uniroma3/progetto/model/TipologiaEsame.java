package it.uniroma3.progetto.model;

public class TipologiaEsame {
	private Long idTipologiaEsame;
	private String nomeTipologiaEsame;
	
	public TipologiaEsame() {}
	
	public Long getIdTipologiaEsame() {
		return idTipologiaEsame;
	}
	
	public void setIdTipologiaEsame(Long idTipologiaEsame) {
		this.idTipologiaEsame = idTipologiaEsame;
	}
	
	public String getNomeTipologiaEsame() {
		return nomeTipologiaEsame;
	}
	
	public void setNomeTipologiaEsame(String nomeTipologiaEsame) {
		this.nomeTipologiaEsame = nomeTipologiaEsame;
	}
 
}
